#Program for Demonstrating Arithmetic Operators
#ArithmeticOpEx1.py
a=float(input("Enter Value of a:"))
b=float(input("Enter Value of b:"))
print("*"*50)
print("\t\tResults of Arithmertic Operators")
print("*"*50)
print("\t\tSum({},{})={}".format(a,b,a+b))
print("\t\tSub({},{})={}".format(a,b,a-b))
print("\t\tMul({},{})={}".format(a,b,a*b))
print("\t\tDiv({},{})={}".format(a,b,a/b))
print("\t\tFloorDiv({},{})={}".format(a,b,a//b))
print("\t\tMod({},{})={}".format(a,b,a%b))
print("\t\tPow({},{})={}".format(a,b,a**b))
print("*"*50)
#here a+b, a-b , a*b ,  a/b , a//b  a%b and a**b
# are called Arithmetic Expressions
