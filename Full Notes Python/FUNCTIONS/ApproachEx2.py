def addop():
    #Take Input
    k=float(input("Enter First Value:"))
    v=float(input("Enter Second Value:"))
    #Do the Process
    r=k+v
    #Display the result in Function Body only
    print("sum({},{})={}".format(k,v,r))

#Main Program
addop() # Function Call without Inputs
