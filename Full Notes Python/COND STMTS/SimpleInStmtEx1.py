#Program for Deciding weather the Given Number +Ve or -Ve or Zero
#SimpleInStmtEx1.py
n=float(input("Enter a Number:")) # n--- 123
if(n>0):
    print("{} is +VE Value".format(n))
if(n<0):
    print("{} is -VE Value".format(n))
if(n==0):
    print("{} is ZERO".format(n))
