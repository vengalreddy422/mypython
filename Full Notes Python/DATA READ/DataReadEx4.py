#Program for Reading Two Numerical Values and find their product
#DataReadEx4.py
a=input("Enter First Value:")
b=input("Enter Second Value:")
#Convert 'a' and 'b' values into float type
x=float(a)
y=float(b)
#Multiply
z=x*y
#Display the Result
print("First Value={}".format(x))
print("Second Value={}".format(y))
print("Product={}".format(z))
